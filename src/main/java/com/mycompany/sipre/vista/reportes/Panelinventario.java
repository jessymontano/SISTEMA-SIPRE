    private   void  exportarCSV(DefaultTableModel tableModel) {
        String fileName = "inventario_report.csv";

        try (CSVWriter writer = new CSVWriter(new FileWriter(fileName))) {
            // Escribir encabezados
            writer.writeNext(new String[]{"Documento", "Cantidad"});
            // Escribir datos
            for (int i = 0; i < tableModel.getRowCount(); i++) {
                String documento = tableModel.getValueAt(i, 0).toString();
                String cantidad = tableModel.getValueAt(i, 1).toString();
                writer.writeNext(new String[]{documento, cantidad});
            }

            JOptionPane.showMessageDialog(this, "Datos exportados a: " + fileName, "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al exportar los datos a CSV.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
