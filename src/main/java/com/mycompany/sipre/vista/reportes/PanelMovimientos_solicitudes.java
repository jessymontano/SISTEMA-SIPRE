    public  void exportarCsv(DefaultTableModel tableModel, String fileName) {
        try (CSVWriter writer = new CSVWriter(new FileWriter(fileName))) {
            // Escribir encabezados
            String[] headers = {"ID Movimiento", "Tipo Movimiento", "Tipo Documento", "Cantidad", "Fecha", "Usuario"};
            writer.writeNext(headers);

            // Escribir datos
            for (int i = 0; i < tableModel.getRowCount(); i++) {
                String idMovimiento = tableModel.getValueAt(i, 0).toString();
                String tipoMovimiento = tableModel.getValueAt(i, 1).toString();
                String tipoDocumento = tableModel.getValueAt(i, 2).toString();
                String cantidad = tableModel.getValueAt(i, 3).toString();
                String fecha = tableModel.getValueAt(i, 4).toString();
                String usuario = tableModel.getValueAt(i, 5).toString();

                // Crear una fila con los datos
                String[] row = {idMovimiento, tipoMovimiento, tipoDocumento, cantidad, fecha, usuario};
                writer.writeNext(row);
            }

            JOptionPane.showMessageDialog(this, "Datos exportados a: " + fileName, "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al exportar los datos a CSV.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

