    // clase para mostrar botones en la tabla
    class ButtonRenderer extends JButton implements TableCellRenderer {

        public ButtonRenderer() {
            setOpaque(true);
        }

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            if (isSelected) {
                setBackground(table.getSelectionBackground());
            } else {
                setBackground(new java.awt.Color(99, 132, 182));
            }
            ImageIcon icono = new ImageIcon(getClass().getResource("/edit.png"));
            Image image = icono.getImage();
            Image nuevaImagen = image.getScaledInstance(20, 20, Image.SCALE_SMOOTH);
            icono = new ImageIcon(nuevaImagen);
            setIcon(icono);
            return this;
        }
    }

    // clase para hacer que funcionen los botones dentro de la tabla y obtengan la informaci칩n de la solicitud seleccionada
    class ButtonEditor extends DefaultCellEditor {

        private JButton button;
        private boolean isPushed;
        private int selectedRow;

        public ButtonEditor(JCheckBox checkBox) {
            super(checkBox);
            button = new JButton();
            button.setOpaque(true);
            ImageIcon icono = new ImageIcon(getClass().getResource("/edit.png"));
            Image image = icono.getImage();
            Image nuevaImagen = image.getScaledInstance(20, 20, Image.SCALE_SMOOTH);
            icono = new ImageIcon(nuevaImagen);
            button.setIcon(icono);
            button.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    abrirFormulario(selectedRow);
                    fireEditingStopped();
                }
            });
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            selectedRow = row;
            return button;
        }

        @Override
        public Object getCellEditorValue() {
            return "Modificar";  // Retorna un valor v치lido
        }
    }

    // abre un nuevo jDialog con un formulario para editar la informaci칩n de la solicitud
    private void abrirFormulario(int rowIndex) {
        Solicitud solicitud = solicitudes.get(rowIndex);

        btnGuardar.addActionListener(e -> {
            solicitud.setTipoDocumento((String) comboTipo.getSelectedItem());
            
            java.util.Date fechaHoy = java.sql.Date.valueOf(LocalDate.now());
            solicitud.setFecha(fechaHoy);
            solicitud.setMotivo(txtMotivo.getText());

            // actualizar la solicitud con los datos introducidos
            SolicitudController solicitudDAO = new SolicitudController();

            solicitudDAO.actualizarSolicitud(solicitud, respuesta -> {
            });

            llenarTabla();
            jDialog1.dispose();
        });

        jDialog1.setLocationRelativeTo(this);
        jDialog1.setVisible(true);
    }

